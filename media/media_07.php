    <h2>※頭戴感應器 用腦波「寫」書法 </h2>
    <p><font size="3">
	<a href="http://mag.udn.com/mag/digital/storypage.jsp?f_MAIN_ID=320&f_SUB_ID=2942&f_ART_ID=465159"><font color="red">網址http://mag.udn.com/mag/digital/storypage.jsp?f_MAIN_ID=320&f_SUB_ID=2942&f_ART_ID=465159</font></a><br>
【聯合報╱記者鄭國樑／桃園報導／2013/07/09】<br>

國際動漫大展腦波寫書法相當有趣，玩過的明知是科技產品，但操作起來很像真的用「念力」寫書法。<br>
<img src="images/20130709_01.jpg"><br>
 記者鄭國樑／攝影<br>

不用揮毫，透過腦波感應器一樣可以寫書法？桃園國際動漫大展請來科技產業界助陣，「大寶腦波」展示區相當吸引人，只要掛上類似頭戴式耳機設備的感應器，當腦波「心平氣和」時就可以看見眼前的螢幕出現書法字體，讓人稱奇。<br>
「好像用念力寫書法」，一對國小姊弟輪流戴上感應器，兩人試了3 、4次，每次都是第1、2筆劃在螢幕出現後，馬上又消失，最後似乎摸出竅門，姊姊調勻呼吸，試著用腦波「感應」，只見眼前一筆一劃的字體出來了，最後是個「襌」字。<br>
弟弟也靜下心，果然，第一個「心」字在螢幕上寫著，後來還寫出「空」字，電腦評分，給姊弟兩人都是「神」，其他高中、國中生也獲得「好」的評語。
展示場的志工說，這套設備是台灣本土企業研發而成，掌握寫書法時頭腦會產生α波的原理，當戴上感應器時，專心想著自己正在寫書法，讓頭腦產生α波，這個時候螢幕上會出現已經輸入的書法字體單字。<br>
<img src="images/20130709_02.jpg" height="50%" width="80%"><br>
「真的很神奇」，雖然民眾知道不是真的靠念力寫字，但感應器在頭上，眼前書法字像寫字似的一筆一劃產生，讓人覺得很過癮，有些人寫一兩筆，還沒成字就消失，大家打趣說「念力不夠」，寫不出字反而更有趣。<br>
工研院也在現場展出3D動漫軟體成果，有5部用這套軟體製作的短劇讓民眾欣賞，質感不輸夢工廠或迪士尼產品，讓動漫迷見識台灣產業的功力。桃園國際動漫大展即日起至14日，在縣立展演中心舉行，多個攤位都能近距離欣賞或操作。
