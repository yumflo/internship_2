【林志鴻／台中訊／2011/04/27】
 
大寶科技運用腦波晶片技術，成功開發出「腦波耳機」，並能透過互動的花圃軟體培訓專注力。大寶科技／提供

大寶科技日前在台中創意文化園區展出一款用腦波控制的專注花園教學軟體，使用者透過簡易的腦波耳機，即可用專注或放鬆的程度來澆水、施肥，用意念種花。<br>
大寶科技參與經濟部中小企業處主辦的「創業領航，拼亮台灣」商機展示會，展出「雲端腦波教室」，從雲端平台中訓練兒童的專注力，並結合台灣學界優秀的研發能量，與台中教育大學，勤益科技大學及中山醫學院進行產學合作，開發以腦波訓練專注力的全方位解決方案。<br>
大寶科技運用美國神念科技（Neurosky）領先的腦波晶片技術，成功開發出可應用在學術研究、人機介面控制以及兒童專注力訓練的各式產品，其中一款用腦波訓練靜坐的軟體「禪坐月曆」，更以四種語言在全世界發行了數萬套。<br>
畫面中溫馨可愛的花圃，使用者透過「腦波耳機」，依專注力與放鬆度控制，可以栽種出各種不同的花，菊花、百合、紫羅蘭，意志力越專注即可獲得更多的水量澆花，讓花成長，而花朵如果缺乏照顧而枯萎時，可用放鬆的腦波施肥並把花救活。是種可以訓練專注力的產品。
