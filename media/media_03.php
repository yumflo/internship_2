    <h2>※顏色反應大腦狀態 大寶科技揭開人的情緒秘密 </h2>
    <p><font size="3">
	<a href="http://www.bnext.com.tw/focus/view/cid/103/id/26093"><font color="red">網址http://www.bnext.com.tw/focus/view/cid/103/id/26093</font></a><br>
數位時代網站／撰文者：尹俞歡／發表日期：2013/01/11<br>
<img src="images/20130111_01.jpg"><br>

人類的大腦充滿迷團，但現在你不需成為頂尖科學家，也能輕易掌握每個人的大腦。由大寶科技公司所設計的腦波觀想儀，透過簡單的顏色光譜反應大腦狀態，搭配不同的應用模式，讓人類大腦的秘密完全攤在陽光下。<br>
大寶科技公司與美國腦科技研發首屈一指的神念科技合作，由神念授權晶片、再由大寶科技公司設計演算與呈現方式，結合腦波偵測與不同的裝置，將腦波偵測實際應用於日常消費市場中。<br>
其中，由大寶科技自行研發的腦波觀想儀，即是生活化應用腦波科技的例子。使用者在戴上監測儀(耳機)後，身旁的光球便會依他的大腦狀態顯示光譜上不同的顏色，如藍色代表高度專注、紅色代表平靜等。<br>
大寶科技執行長張韶芹指出，透過腦波觀想儀，可以為一些無法用言語形容的心理狀態建立外在可觀察的指標，「為那些只有身體能記住的感覺呈現具體的狀態」。如當家長要培養孩子的專注力時，便可將觀想儀顯示的不同顏色做為情緒指標，讓孩子明白自己的身心正處於什麼狀態，進而訓練他們控制自己的大腦與情緒。<br>
此外，腦波偵測也可運用於生活的各種層面中。如將腦波狀態與居家裝置連結，家中的燈光、音樂等就能隨人當下的心理狀態改變。<br>
大寶科技目前也與交大合作研發腦波易經決策的app，使用者可選擇如市場預測等不同的管理情境，回答一連串有關決策的問題；監測儀(耳機)會同步測量、分析腦波，反映使用在回答問題時的情緒是緊張或平靜。最後，程式會比對作答與腦波監測結果，讓使用者瞭解自己是否真的對決策有信心，或只是表面上胸有成竹而已。
這樣的技術同時也可用於廣告商對焦點團體的監測，透過觀賞者腦波狀態直接的反應，可讓廣告商知道廣告的效度及吸睛處。<br>
張韶芹表示，公司目標希望能研發各種腦波分析的消費型產品，以獲得研究資金，進一步鑽研腦波與情緒的關聯，研發以科學的方式管理情緒的。<br>
