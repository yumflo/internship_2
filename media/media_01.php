    <h2>※尋找專心的音符　勤益科大同步腦波實驗室有答案  </h2>
    <p><font size="3">
	<a href="http://www.cna.com.tw/postwrite/cvpread.aspx?ID=48011#.UeUIGY3Q2rd"><font color="red">網址http://www.cna.com.tw/postwrite/cvpread.aspx?ID=48011#.UeUIGY3Q2rd</font></a><br>
(中央社訊息服務20100104／訊息來源：國立勤益科技大學) <br>
 <img src="images/20100104.jpg">
圖 鄭志敏教授在腦波音樂實驗室裡彈奏鋼琴給參與實驗的學童聽<br>
<a href="http://www.youtube.com/watch?v=g1NdRAITNEc"><font color="red">相關影片：http://www.youtube.com/watch?v=g1NdRAITNEc</font></a><br>

你是否記得在希臘神話中，以美妙的豎琴就能馴服野獸的奧菲斯？到底是什麼樣的音樂，才能使人放鬆或專心呢？勤益科技大學的鄭志敏教授在該校的同步腦波評測實驗室中，進行了一系列以音樂增進孩童專注力為主題的實驗。<br>
鄭志敏教授表示，音樂的旋律、輕重、結構及快慢等因素，對人們的情緒和心境皆有不同程度的影響，如果能找出是什麼樣的組合，能使孩童專心或放鬆，將會對孩童的學習及記憶能力有所幫助。<br>她表示，雖然目前已有許多以專心或放鬆為主的音樂商品，但是如果要以腦波來證實音樂的效果，則需要昂貴的腦波儀和冗長的實驗時間。但現在結合的多人同步腦波評測系統，不但可以一次擷取好幾位孩童的腦波，並可即時取得評測結果，這不僅可以大幅縮短實驗的時間，並可以精確的找出使人專心或放鬆的音樂段落，是一個極有創新性的研究方式。<br>
勤益科技大學於今年年底引進美國Neurosky公司的腦波耳機，並以大寶科技開發的Zigbee同步腦波擷取系統設立了多人同步腦波教學品質評測實驗室。<br>Neurosky的腦波儀器在攜帶式腦波儀中維持世界領先地位，不但為眾多先進國家研究機構所使用，他們所開發的專心度評測程式，也被廣泛的運用在歐美幼教界。<br>
鄭志敏從事音樂教育多年，對音樂的賞析與應用都有精湛的見解。在採訪的過程中，見到她與參與實驗的小學生們親切的互動，並播放她精心選取的音樂曲目，讓現場記者深深感到她為台灣認知科學的領域，又增添了一些繽紛的色彩。
