    <h2>※穿戴科技蒐奇！ 電子紙手錶、腦波直升機吸睛 </h2>
    <p><font size="3">
	<a href="http://news.pchome.com.tw/society/ettv/20130605/print-13704155882496617002.html"><font color="red">網址http://news.pchome.com.tw/society/ettv/20130605/print-13704155882496617002.html</font></a><br>
PCHOME新聞／2013/06/05
 <img src="images/20130605_01.jpg"><br>
繼谷歌眼鏡和傳聞中的iwatch之後，今年台北國際電腦Computex掀起一股穿戴科技熱。像是有廠商就推出目前螢幕最大的電子紙智慧型手錶，不僅可以藍芽連結手機讀取訊息、上臉書，還號稱使用七天不充電都沒問題。另外腦波科技也是未來的趨勢，只要戴上儀器就可以用「意志力」操控電腦。<br>
穿戴科技正夯，現在台廠也推出了1.73吋超大電子螢幕錶，標榜使用7天不用充電都沒有問題。除了電子紙螢幕超省電，還可以用藍芽聯結手機APP，用手錶上臉書按讚都不成問題。智慧型手錶業者：「我們這款手錶預計在第三季上市，目前定價是179美元。」<br>
穿戴科技不夠看，Computex上最吸睛的還有用腦波控制的直升機。腦波科技業者：「我們的集中力越高的飛機會飛得越高，超過這個指標的話，那它就會越飛越高。」腦波科技不用動手就可以讓電腦乖乖聽話，但接下來這個可是要讓你全身動起來。<br>
<img src="images/20130605_02.jpg"><br>
以往體感科技只能在大平面的電視上面玩，但是現在只要有這塊小小的晶片，在筆電還有平板上面也可以大玩體感科技。身體左右搖擺，在螢幕上投射出動作就像真的滑板一樣，現在只要一塊晶片USB，體感科技也能帶著走，而且生產成本不到一半。體感科技業者：「運用這樣的科技來操控車內的影音系統，這樣我們在做一些頻道的轉換或是音量控制的時候，手不需要碰到實體的按鍵，這樣對行車安全也有很大的幫助。<br>
如果用小螢幕玩遊戲不過癮試試看這款大的。只是超大視覺享受可不便宜，100吋屏幕加上投影機、音響整套設備就要價1萬美金，相當30萬台幣。投影電視業者：「傳統螢幕適用折射光，所以看久眼睛會不舒服，我們的材料吸收光就會把材料留在那邊，均勻呈現它的色彩度。」電腦展上廠商出奇招也讓人對未來的科技生活充滿期待。
