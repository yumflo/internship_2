    <h2>※教育資訊展 樹林國小登場 </h2>
    <p><font size="3">
	<a href="http://163.26.104.4/www2013ActionLearning/a06-1.htm"><font color="red">網址http://163.26.104.4/www2013ActionLearning/a06-1.htm</font></a><br>
【記者盧萍珊／七股報導／2013/07/16
<img src="images/20130716_01.JPG"><br>
教育資訊展十五日在七股區樹林國小登場，吸引全國各校的資訊人員參加，其中有協助老師檢測專注力不足的軟體，另有超大型的多點觸控螢幕，學生僅在螢幕就能解剖青蛙，增加學習興趣。<br>
<img src="images/20130716_03.JPG"><br>
承辦這次活動的七股樹林國小校長郭耿舜指出，這是全國首次由教育界舉辦的資訊展，共有來自台北、雲林、嘉義、基隆、高雄及南市的一百多位國小資訊老師參加，另邀請十五家資訊業者展示軟硬體設備，包括七股區樹林、西港區成功、新化區那拔、關廟深坑等四所偏鄉國小，也進行行動學習成果發展。<br>
<img src="images/20130716_04.JPG"><br>
籌辦的樹林國小主任楊易霖表示，舉辦這次教育資訊展，主要是學校老師對教育軟體訊息較欠缺，有時採購的東西不一定適合使用，，所以透過這次展示，讓老師多方了解，採購時可以多加考慮。<br>
