    <h2>※2013 Computex BC AWARD 引領智慧新生活（節錄） </h2>
    <p><font size="3">
	<a href="http://www.saydigi.com/2013/05/bc-award-20130528.html"><font color="red">網址http://www.saydigi.com/2013/05/bc-award-20130528.html</font></a><br>
日期：2013/05/28／作者：K小妹
<img src="images/20130528_01.jpg"><br>

每年Computex除了有各大廠的新品令人期待以外，其實還有一個Best Choice Award（簡稱BC Award）引人注目。
原來BC Award 是針對智慧生活應用相關產品所進行的評選活動，因此在BC Award 中，我們也能看到不少融入現在生活的科技新品的出現。根據頒獎現場的體驗，這邊就為大家挑出幾樣的確極具創意又生活化的商品喲！<br>
<b>Mindwave MobileStarter 腦立方移動版</b><br>
 <img src="images/20130528_02.jpg"><br>
 如果有看過我們家報導的朋友應該有印象，先前主編有為大家介紹過Necomimi貓耳，其實這款Mindwave MobileStarter 腦立方移動版就是它的新一代商品。
同樣是透過感應腦波變化來呈現，只是Mindwave MobileStarter 腦立方移動版是透過app來顯示。目前全球約有100多款app支援這樣的裝置來設計不同主題的app。例如有些是設計成可愛的app，來透過集中注意力的遊戲關卡訓練小孩子的專注力。<br>
 <img src="images/20130528_03.jpg"><br>
現場示範了一下，當我們帶起Mindwave Mobile Starter 這裝置後，打開開關選擇這款澳洲所設計的app，挑選關卡主題、想要訓練的項目（這邊選了集中注意力，其他還有像是“放鬆”…等等），就會進入這遊戲畫面。這時玩家會化身成遊戲主角，以這關為例，我們不需要碰觸螢幕只要注意力越集中，魔法能力會越強，當打倒對面的壞巫婆後他就會變成青蛙逃走。<br>
聽起來是不是很酷？！可惜在現場因為要體驗又要專心聽說明人員介紹，因此根本就是我要被打敗了呀！！！當然還有多種遊戲設計可以選擇訓練喲！
